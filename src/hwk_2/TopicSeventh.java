package hwk_2;

public class TopicSeventh {
	public static void main(String[] args) {

		for (int i = 1; i <= 6; i++) {
			switch (i) {
			case 1:
				System.out.println("A");
				break;
			case 2:
				System.out.println("BB");
				break;
			case 3:
				System.out.println("CCC");
				break;
			case 4:
				System.out.println("DDDD");
				break;
			case 5:
				System.out.println("EEEEE");
				break;
			default:
				System.out.println("FFFFFF");
			}
		}
	}
}
